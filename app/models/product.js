function Product(
    id,
    name,
    price,
    screen,
    frontCamera,
    backCamera,
    type,
    image,
    description
  ) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.screen = screen;
    this.frontCamera = frontCamera;
    this.backCamera = backCamera;
    this.type = type;
    this.image = image;
    this.description = description;
  }